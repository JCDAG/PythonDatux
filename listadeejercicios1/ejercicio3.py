#3.Ingrese 3 valores y realice las operaciones de suma ,resta ,multiplicación, división y división entera.
valor1=float(input("Ingrese un numero:"))
valor2=float(input("Ingrese un numero:"))
valor3=float(input("Ingrese un numero:"))
suma=valor1+valor2+valor3
resta=valor1-valor2-valor3
multi=valor1*valor2*valor3
division=valor1/valor2/valor3
divisionentera=valor1//valor2//valor3
print("La suma de los valores es:" , suma)
print("La resta de los valores es:" , resta)
print("La multiplicacion de los valores es:" , multi)
print("La division decimal de los valores es:" , division)
print("La division entera de los valores es:" , divisionentera)

